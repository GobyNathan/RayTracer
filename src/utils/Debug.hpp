#ifndef DEBUG_HPP
#define DEBUG_HPP

#include <iostream>

namespace Raytracer {

class Debug {
private:
    static bool enabled;

public:
    static void setEnabled(bool enable) { enabled = enable; }
    static bool isEnabled() { return enabled; }

    template <typename... Args>
    static void log(Args... args)
    {
        if (enabled) {
            (std::cout << ... << args) << std::endl;
        }
    }
};

}

#endif // DEBUG_HPP
