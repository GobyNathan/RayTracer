// Timer.hpp
#ifndef RAYTRACER_TIMER_HPP_
#define RAYTRACER_TIMER_HPP_

#include <chrono>
#include <string>

namespace Raytracer {

class Timer {
private:
    std::string name;
    std::chrono::high_resolution_clock::time_point startTime;
    bool running;

public:
    Timer(const std::string& name = "Timer");
    ~Timer() = default;

    void start();
    void stop();
    void reset();

    double elapsedSeconds() const;
    std::string elapsedString() const;

    bool isRunning() const { return running; }
    const std::string& getName() const { return name; }
};

} // namespace Raytracer

#endif // RAYTRACER_TIMER_HPP_

